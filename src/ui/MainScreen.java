private void loadProfileData() {
    try {
        java.sql.Connection con = db.OnlineDB.connectOnlineDB();
        String sql = "SELECT * FROM Group9_Employees WHERE Username = ?";
        java.sql.PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, aktifKullanici);
        java.sql.ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            profileUsername.setText(rs.getString("Username"));
            profileName.setText(rs.getString("FirstName"));
            profileSurname.setText(rs.getString("LastName"));
            profileEmail.setText(rs.getString("Email"));
            profilePhone.setText(rs.getString("Phone"));
            profileRole.setText(rs.getString("Role"));
        }
        con.close();
    } catch (Exception e) {
        System.out.println("Profil yükleme hatası: " + e.getMessage());
    }
}

private void loadHalls() {
    try {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tableHalls.getModel();
        model.setRowCount(0); 

        java.sql.Connection con = db.OnlineDB.connectOnlineDB();
        String sql = "SELECT * FROM Group9_Halls";
        java.sql.PreparedStatement pst = con.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("HallID"),
                rs.getString("HallName"),
                rs.getInt("Capacity"),
                rs.getString("HallType")
            });
        }
        con.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Halls update failed: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}

